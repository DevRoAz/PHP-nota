<?php

$x = readline("Digite o primeiro número: ");
$y = readline("Digite o segundo número: ");
$z = readline("Digite o terceiro número: ");


echo "A soma dos numeros é: " . $x + $y + $z . PHP_EOL;
echo "A subtração dos numeros é: " . $x - $y - $z . PHP_EOL;
echo "A multiplicação dos numeros é: " . $x * $y * $z . PHP_EOL;
echo "A divisão dos numeros é: " . $x / $y / $z . PHP_EOL;